print("Média Aritmética de Três Números")
print("================================\n")
num1 = int(input("Olá! Digite um número: "))
num2 = int(input("Agora digite um segundo número: "))
num3 = int(input("E por fim mais um número: "))

media = float((num1 + num2 + num3)/3)

print("\nA média aritmética é ", round(media, 2), sep="")